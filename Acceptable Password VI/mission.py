import re
def is_acceptable_password(password: str) -> bool:

    if len({c for c in password}) < 3:
        return False
    if 'password' in password.lower():
        return False
    elif len(password) > 9:
        return True
    else:
        number_bitmask = [*map(str.isdigit, password)]
        return len(password)>6 and any(number_bitmask) and (False in number_bitmask)

